    private double konversiSuhu(double nilai, String dari, String ke) {
    double hasil = 0;

    // Ubah dulu ke Celcius
    switch (dari) {
        case "Fahrenheit" -> nilai = (nilai - 32) * 5/9;
        case "Reamur" -> nilai = nilai * 5/4;
        case "Kelvin" -> nilai = nilai - 273.15;
    }

        // Dari Celcius ke target
        hasil = switch (ke) {
            case "Fahrenheit" -> (nilai * 9/5) + 32;
            case "Reamur" -> nilai * 4/5;
            case "Kelvin" -> nilai + 273.15;
            default -> nilai;
        };

    return hasil;
}
    
    private void konversiOtomatis() {
        try {
            if (!jTextField1.getText().isEmpty()) {
                double input = Double.parseDouble(jTextField1.getText());
                String dari = jComboBox1.getSelectedItem().toString();
                String ke = jComboBox2.getSelectedItem().toString();

                double hasil = konversiSuhu(input, dari, ke);
                jLabel5.setText(String.format("%.2f", hasil));
            } else {
                jLabel5.setText("-");
            }
        } catch (NumberFormatException e) {
            jLabel5.setText("Input tidak valid");
        }
    }

